/**
 * svg 图标
 */
@Component({
  tag: 'nb-svg-icon',
  styleUrl: 'svg-icon.h5.styl',
  shadow: true
})
export class SvgIcon {
  /**
   * type 图标类型，不使用内置的话不需要设置
   */
  @Prop() type?: string;

  /**
   * svg 大小
   */
  @Prop() size: 'xs' | 's' | 'm' | 'l' | 'xl' = 'm';

  /**
   * 加入动画名称
   */
  @Prop() anim: 'rotate' | '' = '';

  get animation() {
    return this.anim || (this.type && IconSvgs[this.type])
      ? IconSvgs[this.type].anim
      : '';
  }

  render() {
    return (
      <div
        class={`icon ${this.size} ${this.animation}`}
        innerHTML={
          this.type && IconSvgs[this.type] ? IconSvgs[this.type].svg : ''
        }
      >
        <slot />
      </div>
    );
  }
}
